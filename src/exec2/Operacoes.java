package exec2;



/**
 * Exemplo de como fazer as operacoes e imprimir na tela com o comentario
 * @author pbrasileiro
 *@version 0.1
 */


public class Operacoes {
	public static void main(String[] args) {
		
//		exemplo de adição
		int adicao = 5+2;
//		exemplo de subtracao
		int subtracao = 5 - 2;
//		exemplo de multiplicacao
		int multiplicacao = 5*2;
//		exemplo de divisao
		int divisao = 10/2;
//		exemplo do modulo
//		E	uivale a 10 dividido por 3 que dá 3 com resto 1
//		o modulo devolve o valor do resto
		int modulo = 10 % 3;
		
		System.out.println("resultado da adicao\n" + adicao);
		System.out.println("resultado da subtracao\n" + subtracao);
		System.out.println("resultado da multiplicacao\n" + multiplicacao);
		System.out.println("resultado da divisao\n" + divisao);
		System.out.println("resultado da modulo\n" + modulo);
		
		
		
			}

}
